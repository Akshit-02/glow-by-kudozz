export function getPostBySlug(slug: string) {
  return posts.find((post) => post.slug === slug);
}

export function getPostsByCategory(categorySlug: string) {
  return posts.filter((post) => post.categorySlug === categorySlug);
}

export function getPostsByAuthor(authorId: string) {
  return posts.filter((post) => post.authorId === authorId);
}

export function getPostsByTag(tagSlug: string) {
  return posts.filter((post) => post.tagSlugs.includes(tagSlug));
}

export function getRelatedPosts(post: BlogPost, limit = 3) {
  return posts
    .filter((p) => p.id !== post.id && p.categorySlug === post.categorySlug)
    .slice(0, limit);
}
